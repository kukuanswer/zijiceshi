package com.tv.box.model;

public class Track {

	private int isAc;
	private int pid;
	private String language;

	public int getIsAc() {
		return isAc;
	}

	public void setIsAc(int isAc) {
		this.isAc = isAc;
	}

	public int getPid() {
		return pid;
	}

	public void setPid(int pid) {
		this.pid = pid;
	}

	public String getLanguage() {
		return language;
	}

	public void setLanguage(String language) {
		this.language = language;
	}

}
