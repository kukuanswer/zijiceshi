package com.tv.box.model;

public class Wifi {

	private String wifi_ap_name;
	private String wifi_ap_passwd;
	private int free_status;

	public String getWifi_ap_name() {
		return wifi_ap_name;
	}

	public void setWifi_ap_name(String wifi_ap_name) {
		this.wifi_ap_name = wifi_ap_name;
	}

	public String getWifi_ap_passwd() {
		return wifi_ap_passwd;
	}

	public void setWifi_ap_passwd(String wifi_ap_passwd) {
		this.wifi_ap_passwd = wifi_ap_passwd;
	}

	public int getFree_status() {
		return free_status;
	}

	public void setFree_status(int free_status) {
		this.free_status = free_status;
	}

}
