package com.tv.box.version;

public class VersionInfo {

	private String projectName;
	private String platform;
	private String stbHardwareVer;
	private String stbSoftwareVer;
	private String iosMobileAppVer;
	private String androidMobileAppVer;
	
	private String stbFileName;
	private String iosFileName;
	private String stbDevFileName ;
	private String androidFileName;

	public String getProjectName() {
		return projectName;
	}

	public void setProjectName(String projectName) {
		this.projectName = projectName;
	}

	public String getPlatform() {
		return platform;
	}

	public void setPlatform(String platform) {
		this.platform = platform;
	}

	public String getStbHardwareVer() {
		return stbHardwareVer;
	}

	public void setStbHardwareVer(String stbHardwareVer) {
		this.stbHardwareVer = stbHardwareVer;
	}

	public String getStbSoftwareVer() {
		return stbSoftwareVer;
	}

	public void setStbSoftwareVer(String stbSoftwareVer) {
		this.stbSoftwareVer = stbSoftwareVer;
	}

	public String getIosMobileAppVer() {
		return iosMobileAppVer;
	}

	public void setIosMobileAppVer(String iosMobileAppVer) {
		this.iosMobileAppVer = iosMobileAppVer;
	}

	public String getAndroidMobileAppVer() {
		return androidMobileAppVer;
	}

	public void setAndroidMobileAppVer(String androidMobileAppVer) {
		this.androidMobileAppVer = androidMobileAppVer;
	}

	public String getStbFileName() {
		return stbFileName;
	}

	public void setStbFileName(String stbFileName) {
		this.stbFileName = stbFileName;
	}

	public String getIosFileName() {
		return iosFileName;
	}

	public void setIosFileName(String iosFileName) {
		this.iosFileName = iosFileName;
	}

	public String getAndroidFileName() {
		return androidFileName;
	}

	public void setAndroidFileName(String androidFileName) {
		this.androidFileName = androidFileName;
	}

	public String getStbDevFileName() {
		return stbDevFileName;
	}

	public void setStbDevFileName(String stbDevFileName) {
		this.stbDevFileName = stbDevFileName;
	}
	
}
